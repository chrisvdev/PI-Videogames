const server = require("./src/app.js");
const { conn, dataBase } = require("./src/db.js");

// Syncing all the models at once.
const startServer = () => {
  server.listen(8080, () => {
    console.log("Listening at 8080");
  });
};
const startDB = async () => {
  return await conn.sync({ force: false });
};

const start = async () => {
await startDB();
startServer();
dataBase.createGenres();
}

start();